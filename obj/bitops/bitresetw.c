#include "bitops.h"
#include "typedefs.h"
#include <stdio.h>


/*************************************************************************//** 
 * @Descripcion	
 * @param[in]	
 * @test
 * @code (0b1111111111111111, 0)  --> 0b1111111111111110
 *		 (0b1111111111111111, 15) --> 0b0111111111111111
 * @endcode
 ****************************************************************************/
WORD bitresetw(WORD* intflag, char bt)
{
 	if (bt > 7)
		intflag->byte1 &= ~(1 << (bt - 8));

	else
		intflag->byte0 &= ~(1 << bt);
	
	return *intflag;
}//

