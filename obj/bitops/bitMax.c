#include "bitops.h"
#include "typedefs.h"
#include <stdio.h>



/*************************************************************************//** 
 * @test
 * @code                                                              
 	(0b0001000) -> 4
 	(0b1000000) -> 8
 	(0b0000001) -> 1
 	(0b0000000) -> 0
  @endcode
****************************************************************************/
byte maxBit(byte mask)
{
    byte ret = 1, shifter = mask;
        
    if (!mask) 
    	return 0;

    while (shifter >>= 1) // sh = sh >> 1
        ret++;

    return ret;
} //
