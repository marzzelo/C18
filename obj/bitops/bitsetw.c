#include "bitops.h"
#include "typedefs.h"
#include <stdio.h>


/*************************************************************************//** 
 * @Descripcion	
 * @param[in]	
 * @test
 * @code (0b0000000000000000, 0)  --> 0b0000000000000001
 *		 (0b0000000000000000, 15) --> 0b1000000000000000
 * @endcode
 ****************************************************************************/
WORD bitsetw(WORD* intflag, char bt)
{
 	if (bt > 7)
		intflag->byte1 |= (1 << (bt - 8));
	
	else
		intflag->byte0 |= (1 << bt);
	
	return *intflag;
}//

